<?php

namespace JAL\Bundle\TaskBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use JAL\Bundle\TaskBundle\Model\StateInterface;

/**
 * JAL\Bundle\TaskBundle\Entity\State
 *
 * @ORM\Table()
 * @ORM\Entity
 */
class State implements StateInterface
{
    /**
     * @var integer $id
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @ORM\Column(type="string")
     */
    protected $name;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return State
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    public function __toString()
    {
        return $this->getName();
    }
}
